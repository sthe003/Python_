#operacoes de multiplicacao, subtracao e adicao

numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

soma = numero1 + numero2
print("A soma entre os dois número resulta em: ", soma)
subtracao = numero1 - numero2
print("A subtracao entre os dois números resulta em: ", subtracao)
multiplicacao = numero1 * numero2
print("A multiplicação entre os dois números resulta em: ", multiplicacao)